// Declare a variable
// To save information, we use variables. 
let myVariable;

// You can also put a number in a variable:
let myNumber = 42;

// Or put some text in a variable (which is called a string):
let myString = "Hello, World!";

// Difference between a number and a string
// A number is for math, like 42.
// let myNumber = 42;

// A string is for text, like "Hello, World!".

// Separate variables for first name, last name, and full name
// You can store names in variables too.
let firstName = "Salman";
let lastName = "Omar";

// To make a full name, you can put the first name and last name together with a space in between:
let fullName = firstName + " " + lastName;

// Let's see what's in these variables:
console.log("First Name:", firstName);
console.log("Last Name:", lastName);
console.log("Full Name:", fullName);
